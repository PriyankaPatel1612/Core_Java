class BitwiseOperators2
{
  public static void main(String args[])
  {
    int a=10|6;          //Bitwise OR example
    System.out.println(a);          //output:14
   }
}

/* 
    binary of 10 is 1010
    binary of 6   is 0100
    (in OR 10 is 1, 01 is 1, 00 is 0, 11 is 1)
    answer is 14    (1110)
*/