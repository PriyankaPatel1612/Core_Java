class BitwiseOperators1
{
  public static void main(String args[])
    {
       int a=10&7;                     //Bitwise AND Example
       System.out.println(a);         //output:2 
    }
}

/* How bitwise AND (&) operator works
    from the above example
    a=10&7
    binary of 10 is 1010
    binary of 7 is 0111
    (In AND 11 is 1, 01 is 0, 10 is 0, 00 is 0)
    then by calculating binary of 10 (1010) and 7 (0111) it becomes 2(0010)
    Answer is 2
*/
    


